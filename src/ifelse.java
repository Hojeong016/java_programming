public class ifelse {
    public static void main(String[] args) {
        int money = 5000;
        int hamBerGer = 6500;
        if(money >= hamBerGer){
            System.out.println("냠냠 햄버거 맛있다.");
        }else if(money >= 5000){
            System.out.println("냠냠 맛있다 스테이크");
        }else {
            System.out.println("꼬르륵 배고프다");
        }
    }
}
